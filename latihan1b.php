<?php
// Deklarasi variabel
$angka = 10;
echo "Aku adalah angka $angka.<br>";

// Operasi perkalian
$angka *= 8;
echo "Jika aku di kali 8, jumlahku sekarang $angka.<br>";

// Operasi pembagian
$angka /= 4;
echo "Jika aku di bagi 4, jumlahku sekarang $angka.<br>";

// Operasi pengurangan
$angka -= 6;
echo "Jika aku di kurang 6, jumlahku sekarang $angka.<br>";

// Operasi penambahan
$angka += 2;
echo "Jika aku di tambah 2, jumlahku sekarang $angka.<br>";
?>